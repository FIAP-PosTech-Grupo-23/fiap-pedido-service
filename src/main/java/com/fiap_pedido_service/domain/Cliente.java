package com.fiap_pedido_service.domain;

public class Cliente {
    private Long id;
    private Long nome;
    private Long cpf;
    private Long endereco;

    public Long getId() {
        return id;
    }

    public Long getNome() {
        return nome;
    }

    public Long getCpf() {
        return cpf;
    }

    public Long getEndereco() {
        return endereco;
    }
}
